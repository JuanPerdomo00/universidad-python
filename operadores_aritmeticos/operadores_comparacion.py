#!/usr/bin/python3

a, b = int(input("Ingrese un numero: ")), int(input("Ingrese un numero: "))

resultado = a == b
print(f"Resultado: == {resultado}")

resultado = a != b
print(f"Resultado: != {resultado}")

resultado = a > b
print(f"Resultado > : {resultado}")

resultado = a >= b
print(f"Resultado >= : {resultado}")

resultado = a < b
print(f"Resultado < : {resultado}")

resultado = a <= b
print(f"Resultado <= : {resultado}")
